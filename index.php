<td valign="top">Strangeville is a turn-based, web-based, multi-player game set in a small city where competing sides will try to outlast each other and the clock.</td>
<td valign="top">Each player in a game will start with several units that correspond to their side. Players may assign orders to each unit at any time but orders are only processed at the top of the hour. Each unit may only carry out one order every 24 hours. The game never stops and orders are carried out around the clock. Only allowing units to be ordered once in a 24 hour time period allows players to sleep, eat, work, etc.. while the battle is raging without falling behind.</td>
